    @MainActor
    static let preview: PersistenceController = {
        let result = PersistenceController(inMemory: true)
        let viewContext = result.container.viewContext
        let samples: [(Double, String, String, String, String)] = [
            (12.49, "Food", "Cash", "Lunch burrito", "fork.knife"),
            (35.00, "Transport", "Card", "Taxi to airport", "car.fill"),
            (79.99, "Shopping", "Card", "Groceries", "bag.fill")
        ]
        for s in samples {
            let e = Expense(context: viewContext)
            e.amount = s.0
            e.category = s.1
            e.date = Date()
            e.paymentMethod = s.2
            e.note = s.3
            e.symbolName = s.4
        }
        do {
            try viewContext.save()
